/* 1.click([data],fn)  / dblclick([data],fn)   单击和双击，
                        绑定在同一事件上时，双击会触发单击

*/

/* 2.scroll(fn)   在某一元素上滚动(需有滚动条)时，触发事件
*/

/* 3.mousedown(fn) /mouseup(fn)   鼠标在 按下 / 放松 对应元素时触发 
*/

/* 4.mouseenter(fn) /mouseleave(fn)   鼠标在 移入 / 移出 对应元素触发 
*/

/* 5.mouseover(fn) /mouseout(fn)   鼠标在 移入 / 移出 对应元素及其子元素时触发 (不常用)
*/

/* 6.hover(fn1,fn2)  
        (1).只写一个fn 鼠标 停留 在对应元素时触发
        (2). 两个fn时，第一个为 进入 时触发的事件，第二个为 移出 时触发的事件(多用于进入时变化，离开时还原)
*/

/* 7.mousemove([data],fn)   鼠标在 对应元素内移动时 触发 (不常用)
*/
